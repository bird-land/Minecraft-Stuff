function reign(monitor,speed)
	textutils.slowPrint("Is this the Region, this the Soil, the Clime,", speed)
	textutils.slowPrint("Said then the lost Arch-Angel, this the seat", speed)
	textutils.slowPrint("That we must change for Heav'n, this mournful gloom", speed)
	textutils.slowPrint("For that celestial light? Be it so, since he", speed)
	textutils.slowPrint("Who now is Sovran can dispose and bid", speed)
	textutils.slowPrint("What shall be right: fardest from him is best", speed)
	textutils.slowPrint("Whom reason hath equald, force hath made supream", speed)
	textutils.slowPrint("Above his equals. Farewel happy Fields", speed)
	textutils.slowPrint("Where Joy for ever dwells: Hail horrours, hail", speed)
	textutils.slowPrint("Infernal world, and thou profoundest Hell", speed)
	textutils.slowPrint("Receive thy new Possessor: One who brings", speed)
	textutils.slowPrint("A mind not to be chang'd by Place or Time.", speed)
	textutils.slowPrint("The mind is its own place, and in it self", speed)
	textutils.slowPrint("Can make a Heav'n of Hell, a Hell of Heav'n.", speed)
	textutils.slowPrint("What matter where, if I be still the same,", speed)
	textutils.slowPrint("And what I should be, all but less then he", speed)
	textutils.slowPrint("Whom Thunder hath made greater? Here at least", speed)
	textutils.slowPrint("We shall be free; th' Almighty hath not built", speed)
	textutils.slowPrint("Here for his envy, will not drive us hence:", speed)
	textutils.slowPrint("Here we may reign secure, and in my choyce", speed)
	textutils.slowPrint("To reign is worth ambition though in Hell:", speed)
	textutils.slowPrint("Better to reign in Hell, then serve in Heav'n", speed)
end
